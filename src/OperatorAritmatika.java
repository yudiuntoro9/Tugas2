public class OperatorAritmatika {
public int Hasil ;
int penjumlahan  (int a, int b){
      
    Hasil = a + b;
    return Hasil;
}
int pengurangan(int a,int b ){
    Hasil = a - b;
    return Hasil;
    
}
int perkalian (int a, int b){
    Hasil = a * b;
    return Hasil;
    
}
double pembagian(double a, double b){
    double Hasil = a/ b;
    return Hasil;
}
}
